
var myValue1 = null;
console.log(myValue1);
if(myValue1){
    console.log('Good')
} else {
    console.log('Bad')
}


var myValue2; // Undefined
console.log(myValue2);
if(myValue2){
    console.log('Good')
} else{
    console.log('Bad')
}

/*
    null means absence of value.
    undefined means lack of value
    null and undefined value evaluates to false in conditional expression
*/

